package chapter1;
/*
 * 
 * Write a program that displays Welcome to Java five times.
 * 
 * Created by Ryan Simpson 7/6/18
 */

public class E1_02DisplayFiveMessages {
	public static void main(String[] args) {
		int i = 0;
		while (i < 5) {
			System.out.println("Welcome to Java");
			i++;
		}
	}
}